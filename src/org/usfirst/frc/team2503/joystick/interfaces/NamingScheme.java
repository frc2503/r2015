/**
 * Naming Joystick Interfaces:
 * 
 * Axis:
 * 
 *  LeftRight: Has left-right axis value functions.
 *  ForwardBack: Has forward-back axis value functions.
 *  Scroll: Has a scroll wheel (up, down).
 *  Rotate: Has rotation axis value functions (left, right).
 *  SingleThrottle: Has one singular throttle with axis value functions.
 *  DoubleThrottle: Has two throttles with axis value functions.
 * 
 * Button:
 * 
 *  Trigger: Has a trigger button on the stick.
 *  Pov: Has a POV switch.
 */